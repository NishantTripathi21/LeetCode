class Solution {
public:
    int minimumDistance(vector<int>& nums) {
        int n = nums.size();
        int maxi= INT_MAX;
        for(int i= 0; i< n; i++) {
            for(int j= i+1; j< n; j++) {
                for(int k = j+1; k< n; k++) {
                    if(nums[i] == nums[j] && nums[j]==nums[k]) {
                        //cout<<i<<" "<<j<<" "<<k<<" "<<maxi;
                        maxi = min(maxi, abs(i-j)+abs(j-k)+abs(k-i));
                    }
                }
            }
        }
        return maxi==INT_MAX?-1:maxi;
    }
};
